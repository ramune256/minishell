#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	dummy;

	if (n == -2147483648)
	{
		dummy = write(fd, "-2147483648", 11);
		(void)dummy;
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	ft_putchar_fd(n % 10 + '0', fd);
}
