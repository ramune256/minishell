#include "minishell.h"

void	free_tokens(t_token *token)
{
	t_token	*tmp;

	while (token)
	{
		tmp = token->next;
		free(token->value);
		free(token);
		token = tmp;
	}
}

bool	is_metachar(char c)
{
	return (c == '|' || c == '<' || c == '>');
}
